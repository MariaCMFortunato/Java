package exemplo05_for;

/**
 *
 * @author Maria11
 */
public class Exemplo05_For {

    /**
     * @param args the command line arguments
     */
    public static void main(String[] args) {
        for (int i = 0; i < 10; i++){
            System.out.println(i + 1);
        }
        int j = 0;
        while(j < 10)
        {
            System.out.println(j + 1);
            j++;
        }
    }
    
}
