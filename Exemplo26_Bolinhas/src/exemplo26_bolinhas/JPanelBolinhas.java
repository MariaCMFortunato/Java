    public JPanelBolinhas() {
        initComponents();
        this.setPreferredSize(new Dimension(LARGURA, ALTURA));
        
        Thread gameThread = new Thread(){
            public void run(){
                int j;
                for(j=0; j < QUANTIDADE; j++)
                    b1[j]= new Classe_BolinhaComCor();
                
                while(true){
                    for(j=0; j < QUANTIDADE; j++)
                    {
                        b1[j].setBallX(b1[j].getBallX()+ b1[j].getBallSpeedX());
                        b1[j].setBallY(b1[j].getBallY()+ b1[j].getBallSpeedY());
                        if (b1[j].getBallX()< 0)
                            b1[j].setBallSpeedX(-b1[j].getBallSpeedX());
                        if(b1[j].getBallX() > LARGURA-2*b1[j].Radius)
                             b1[j].setBallSpeedX(-b1[j].getBallSpeedX());
                        
                        if (b1[j].getBallY()< 0)
                            b1[j].setBallSpeedY(-b1[j].getBallSpeedY());
                        if(b1[j].getBallY() > ALTURA-2*b1[j].Radius)
                             b1[j].setBallSpeedY(-b1[j].getBallSpeedY());
                    }
                    repaint();
                    try
                    {
                        Thread.sleep(10);
                    }
                    catch(Exception ex)
                    {
                    
                    }
                }
            }
        };
        gameThread.start();
    }
        
        @Override
        public void paintComponent(Graphics g)
        {
            super.paintComponent(g);//vai buscar componnets da classe m√£e JPanel
            g.setColor(Color.BLUE);
            g.fillRect(0,0, LARGURA, ALTURA);
            int j;
            for (j=0; j< QUANTIDADE; j++)
            {
                g.setColor(new Color(b1[j].getR(),b1[j].getG(),b1[j].getB()));
                g.fillOval((int)(b1[j].getBallX()), (int)(b1[j].getBallY()), (int)(2 * b1[j].Radius), (int)(2 * b1[j].Radius));
            }
        }   
   
        


