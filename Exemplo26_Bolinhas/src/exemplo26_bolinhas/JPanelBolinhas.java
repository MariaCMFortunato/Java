    private float ballRadius = 20;
    private final static int QUANTIDADE=100;
    private final static int LARGURA=1000;
    private final static int ALTURA=700;
    //Classe_Bola[] b1 = new Classe_Bola[QUANTIDADE];
    Classe_BolinhaComCor[] b1 = new Classe_BolinhaComCor[QUANTIDADE];
    
